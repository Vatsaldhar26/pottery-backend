package uk.ac.cam.cl.dtg.teaching.pottery.exceptions;

public class TaskCloneException extends Exception {

	private static final long serialVersionUID = 1L;

	public TaskCloneException() {
		super();
	}

	public TaskCloneException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public TaskCloneException(String message, Throwable cause) {
		super(message, cause);
	}

	public TaskCloneException(String message) {
		super(message);
	}

	public TaskCloneException(Throwable cause) {
		super(cause);
	}

	
}
